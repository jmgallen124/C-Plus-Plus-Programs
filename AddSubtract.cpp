#include <iostream>
#include <string>
using namespace std;

// Main program to demonstrate basic input and output
int main() {
    
	// Declare integers
	int x, y;
	
	// Get first number
	cout << "Enter a number: ";
	cin >> x;
	
	// Get second number
	cout << "Enter another number: ";
	cin >> y;
	
	// Output addition of numbers
	cout << "Both numbers added equal: " << x + y << endl;
	
	// Output subtraction of numbers
	cout << "Botn numbers subtracted equal: " << x - y << endl;
	
	// End program
	return 0;
}
